function linearSearch(arr, target) {
    // Проходим по каждому элементу массива
    for (let i = 0; i < arr.length; i++) {
        // Если текущий элемент равен целевому, возвращаем его индекс
        if (arr[i] === target) {
            return i;
        }
    }
    // Если элемент не найден, возвращаем -1
    return -1;
}

// Пример использования
const array = [3, 1, 4, 1, 5, 9, 2, 6];
console.log(linearSearch(array, 5)); // Вернет 4 (индекс числа 5)
console.log(linearSearch(array, 7)); // Вернет -1 (число 7 отсутствует)

// Сложность - O(n)

// Память - O(1)

// Особенности: не требует предварительной сортировки, подходит для небольших массивов